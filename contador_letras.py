# Paso 1: Solicitar al usuario que ingrese la palabra.

palabra_ingresada = input("Por favor, ingrese la palara: ")

# Paso 2: Contamos la cantidad de letras en la palabra ingresada --> Fc = Len

cant_letras = len(palabra_ingresada)

# Paso 3: Mostramos al usuario la cantidad de letras  

print ("La palabra ingresada tiene", cant_letras, "letras.")